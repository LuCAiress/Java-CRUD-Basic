    private void carregaTabela(){
        DefaultTableModel dados = (DefaultTableModel) jTAB.getModel();
        dados.setNumRows(0);
        jTAB.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTAB.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTAB.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTAB.getColumnModel().getColumn(3).setPreferredWidth(30);
        
        try{  
            pst = conexao.prepareStatement("select * from estoque;");
            rs = pst.executeQuery();
            
            while(rs.next()){
                dados.addRow(new Object[]{
                rs.getInt(1),
                rs.getString(2),
                rs.getString(3),
                rs.getInt(4)
                });
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    
}
