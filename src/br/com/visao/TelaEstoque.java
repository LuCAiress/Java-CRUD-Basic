    private void salvar() {
        String sql = "insert into estoque (cod, nome, tipo, quant) values(?,?,?,?)";
        try{
           pst = conexao.prepareStatement(sql);
           pst.setInt(1, Integer.parseInt(jTcod.getText()));
           pst.setString(2, jTnome.getText());
           pst.setString(3, jTtipo.getText());
           pst.setInt(4, Integer.parseInt(jTest.getText()));
           System.out.println(pst);
           
           int resultado = pst.executeUpdate();
           if(resultado == 1){
              JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
              jTcod.setText("");
              jTnome.setText("");
              jTtipo.setText(""); //pode ser null ao invés de ""
              jTest.setText("");
           }else{
               JOptionPane.showMessageDialog(null, "Impossível cadastrar este produto!");
           }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        carregaTabela();
    }

    private void excluir() {
        String sql = "DELETE FROM estoque where cod = ?";
        try{
            pst = conexao.prepareStatement(sql);
            pst.setString(1, jTcod.getText());
            System.out.println(pst);
            
            int resultado = pst.executeUpdate();
            if(resultado == 1){
                JOptionPane.showMessageDialog(null, "Produto deletado com sucesso!");
                jTcod.setText("");
                jTnome.setText("");
                jTtipo.setText("");
                jTest.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Impossível deletar este produto!.");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        carregaTabela();
    }



    private void atualizar() {
        String sql = "update estoque set nome=?, tipo=?, quant=? where cod=?";
        try{
            pst = conexao.prepareStatement(sql);
            pst.setInt(4, Integer.parseInt(jTcod.getText()));
            pst.setString(1, jTnome.getText());
            pst.setString(2, jTtipo.getText());
            pst.setInt(3,Integer.parseInt(jTest.getText()));
           
            System.out.println(pst);
           
            int atualizado = pst.executeUpdate();
            if(atualizado == 1){
                JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!");
                
                jTnome.setText(null);//apaga o campo preenchido
                jTtipo.setText(null);
                jTest.setText(null);
            }else{
                JOptionPane.showMessageDialog(null, "Impossível atualizar este produto!");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        carregaTabela();
    }
    
